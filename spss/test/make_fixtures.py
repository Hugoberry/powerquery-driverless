# Regenerates the .sav / .zsav fixtures in this directory.
#
#   python3 -m venv venv && venv/bin/pip install pyreadstat
#   venv/bin/python make_fixtures.py
#
# Most fixtures are written with pyreadstat (a wrapper over ReadStat, the
# reference reader/writer used by R haven and Python). Two are hand-crafted
# byte by byte because ReadStat cannot write them: a legacy codepage-1252
# file with no character-encoding record, and a multi-block .zsav. Both are
# read back with pyreadstat after writing, so every fixture in this
# directory is confirmed readable by the reference implementation.
#
# All fixtures are synthetic and generated by this script; no third-party or
# customer data. Reruns are byte-identical except for the creation date/time
# ReadStat stamps into the 176-byte header.

import struct
import zlib
from pathlib import Path

import pandas as pd
import pyreadstat

HERE = Path(__file__).parent


def write(name, df, **kw):
    path = HERE / name
    pyreadstat.write_sav(df, str(path), **kw)
    back, meta = pyreadstat.read_sav(str(path), user_missing=True)
    assert len(back) == len(df), name
    print(f"{name}: {len(back)} rows x {len(back.columns)} cols, "
          f"{path.stat().st_size} bytes, compression={header_compression(path)}")
    return back, meta


def header_compression(path):
    with open(path, "rb") as f:
        h = f.read(80)
    return struct.unpack("<i", h[72:76])[0]


# --- types: every column shape, in all three compression modes -------------------
#
# Numeric values chosen for bytecode coverage: integers in [-99, 151] compress
# to a single command byte (code = value + bias, bias 100); anything else is a
# 253-literal; None is system-missing (255); an all-spaces string is 254.

types_df = pd.DataFrame({
    "id": [1.0, 2.0, 3.0, 4.0],                       # command-byte integers
    "score": [-99.0, 151.0, 0.5, None],               # bias edges, literal, sysmis
    "big": [1e300, -2.75, 9007199254740992.0, 0.0],   # 253-literals
    "str8": ["", "abc", "héllo ✓", "x"],              # single-element string
    "str20": ["", "twenty char string!!", "short", "padded      end"],
})

write("types.sav", types_df)                          # compression 0
write("rowcomp.sav", types_df, row_compress=True)     # compression 1 (bytecode)
write("types.zsav", types_df, compress=True)          # compression 2 (zlib)

